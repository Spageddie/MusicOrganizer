package prj5;

/**
 * Interface for the ADT LinkedList
 * 
 * @author Matthew Marquez (mmarquez)
 * @version 2017.11.15
 * @param <T>
 *            generic type for LinkedList
 */
public interface LinkedInterface<T> {
    public void clear();
    public boolean isEmpty();
    public void add();
    public T remove();
}
